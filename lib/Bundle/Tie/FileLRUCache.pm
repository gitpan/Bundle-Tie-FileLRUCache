=head1 NAME

Bundle::Tie::FileLRUCache - A bundle to install all Tie::FileLRUCache related modules

=head1 SYNOPSIS

C<perl -MCPAN -e 'install Bundle::Tie::FileLRUCache'>

=head1 CONTENTS

Digest::MD5             - For Tie::DB_File::SplitHash and Tie::FileLRUCache

Class::NamedParms       - Used by most of the system

Class::ParmList         - Used by most of the system

Tie::FileLRUCache       - The Tie::FileLRUCache module itself 

=head1 DESCRIPTION

This bundle includes the Tie::FileLRUCache module and all modules
that it depends on.

